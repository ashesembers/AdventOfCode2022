def parse_stacks(CRATES):
    stacks = []
    for i in range(1,10):
        stacks.append([])

    # read the stuff
    for line in CRATES.split("\n")[:-1]:
        for i in range(1, 10):
            stacks[i-1].append(line[i*4-4:i*4])

    # trim the guff
    for i in range(len(stacks)):
        for j in range(len(stacks[0])):
            stacks[i][j] = stacks[i][j].strip().replace("[","").replace("]","")

    # clear the blanks
    for stack in stacks:
        while stack[0] == "":
            stack.pop(0)

    return stacks


def parse_moves(moves_str):
    moves = []
    for line in moves_str.split("\n"):
        if not line:
            continue
        _, count, _, frm, _, to = line.split(" ")
        moves.append({"count": count, "frm":frm, "to":to})
    return moves


def do_the_moves(stacks, moves):
    for move in moves:
        frm = int(move.get("frm"))-1
        to = int(move.get("to"))-1
        count = int(move.get("count"))
        for i in range(count):
            crate = stacks[frm].pop(0)
            stacks[to].insert(0, crate)
    return stacks


